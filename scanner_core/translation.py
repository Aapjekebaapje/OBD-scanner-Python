def _deep_merge(base: dict, overrides: dict) -> dict:
    merged = deepcopy(base)
    for key, value in overrides.items():
        if isinstance(value, dict) and isinstance(merged.get(key), dict):
            merged[key] = _deep_merge(merged[key], value)
        else:
            merged[key] = deepcopy(value)
    return merged


def _resolve_key(data: dict, key: str):
    current = data
    for part in key.split("."):
        if not isinstance(current, dict) or part not in current:
            return None
        current = current[part]
    return current


def get_language(requested: str | None) -> str:
    code = str(requested or DEFAULT_LANGUAGE).strip().lower()
    return code if code in SUPPORTED_LANGUAGE_CODES else DEFAULT_LANGUAGE


def get_translations(lang: str) -> dict:
    code = get_language(lang)
    base = TRANSLATIONS["en"]
    if code == "en":
        return deepcopy(base)
    return _deep_merge(base, TRANSLATIONS.get(code, {}))


def translate(lang: str, key: str, **kwargs):
    data = get_translations(lang)
    value = _resolve_key(data, key)
    if value is None:
        return key
    if isinstance(value, str) and kwargs:
        try:
            return value.format(**kwargs)
        except Exception:
            return value
    return value


